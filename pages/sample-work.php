    <h3>Customer Segmentation & Prediction Strategy</h3>
    <details>
      <summary>Approach</summary>
      <ul>
        <li><strong>Input Data:</strong> A customer dataset containing annual income and spending data.</li>
        <li><strong>Clustering:</strong> Customers are grouped into three segments using machine learning-based clustering logic:
          <ul>
            <li>High Income – High Spending</li>
            <li>Medium Income – Low Spending</li>
            <li>Low Income – High Spending</li>
          </ul>
        </li>
        <li><strong>Visualization:</strong> A scatter plot is used to visually represent the clusters.</li>
        <li><strong>Prediction:</strong> Spending patterns are predicted based on income using linear regression, leveraging machine learning algorithms.</li>
      </ul>
    </details>

    <details>
      <summary>Business Applications</summary>
      <ul>
        <li><strong>Market Segmentation:</strong> Tailor marketing strategies to specific customer segments based on behavior.</li>
        <li><strong>Customer Retention:</strong> Enhance loyalty through personalized and targeted retention programs.</li>
        <li><strong>Pricing Optimization:</strong> Adjust pricing strategies and discounts to suit the needs of each segment.</li>
        <li><strong>Market Prediction:</strong> Anticipate customer spending trends using machine learning-driven income-spending predictions.</li>
      </ul>
